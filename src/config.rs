use anyhow::Result;
use serde::{Deserialize, Serialize};
use std::io::{self, Write};

/// Project-level configuration stored in `codexa.toml`
#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct Config {
    pub project: ProjectConfig,
    pub scan: ScanConfig,
    pub output: OutputConfig,
    /// Optional vector database — disabled by default
    #[serde(default)]
    pub vector: Option<VectorConfig>,
}

// ── Vector DB config ──────────────────────────────────────────────────────────

#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct VectorConfig {
    /// Which backend to use
    #[serde(default)]
    pub backend: VectorBackend,
    /// Embedding provider
    #[serde(default)]
    pub embedding: EmbeddingProvider,
    /// Chunk size in lines
    #[serde(default = "default_chunk_size")]
    pub chunk_size: usize,
    /// Overlap between chunks in lines
    #[serde(default = "default_overlap")]
    pub overlap: usize,
    /// Embedding batch size (how many chunks sent per API call)
    #[serde(default = "default_batch_size")]
    pub batch_size: usize,
}

fn default_chunk_size() -> usize { 60 }
fn default_overlap() -> usize { 10 }
fn default_batch_size() -> usize { 32 }

impl Default for VectorConfig {
    fn default() -> Self {
        VectorConfig {
            backend: VectorBackend::Sqlite {
                path: ".codexa/index.db".into(),
            },
            embedding: EmbeddingProvider::Ollama {
                base_url: "http://localhost:11434".into(),
                model: "nomic-embed-text".into(),
                dimensions: 768,
            },
            chunk_size: default_chunk_size(),
            overlap: default_overlap(),
            batch_size: default_batch_size(),
        }
    }
}

/// Which vector store backend to use.
#[derive(Debug, Serialize, Deserialize, Clone, Default)]
#[serde(tag = "type", rename_all = "lowercase")]
pub enum VectorBackend {
    /// No vector DB — context generated by re-scanning every time
    #[default]
    None,
    /// SQLite with sqlite-vec extension (zero setup, single file)
    Sqlite {
        /// Path to the .db file (relative to project root)
        path: String,
    },
    /// PostgreSQL with pgvector extension
    Postgres {
        /// Full libpq connection string, e.g. "host=localhost dbname=codexa user=dev"
        connection_string: String,
    },
}

/// Where to fetch embeddings from.
#[derive(Debug, Serialize, Deserialize, Clone)]
#[serde(tag = "provider", rename_all = "lowercase")]
pub enum EmbeddingProvider {
    /// OpenAI embeddings API
    OpenAI {
        api_key: String,
        #[serde(default = "default_openai_model")]
        model: String,
    },
    /// Any OpenAI-compatible API (Together, Mistral, local LM Studio, etc.)
    #[serde(rename = "openai_compatible")]
    OpenAICompatible {
        api_base: String,
        #[serde(default)]
        api_key: Option<String>,
        model: String,
        #[serde(default = "default_dimensions")]
        dimensions: usize,
    },
    /// Ollama — runs locally, no API key needed
    Ollama {
        #[serde(default = "default_ollama_url")]
        base_url: String,
        #[serde(default = "default_ollama_model")]
        model: String,
        #[serde(default = "default_dimensions")]
        dimensions: usize,
    },
    /// Local ONNX — runs locally using ort + tokenizers
    #[serde(rename = "local_onnx")]
    LocalOnnx {
        model_path: String,
        tokenizer_path: String,
        #[serde(default = "default_dimensions")]
        dimensions: usize,
    },
}

fn default_openai_model() -> String { "text-embedding-3-small".into() }
fn default_ollama_url() -> String { "http://localhost:11434".into() }
fn default_ollama_model() -> String { "nomic-embed-text".into() }
fn default_dimensions() -> usize { 768 }

impl Default for EmbeddingProvider {
    fn default() -> Self {
        EmbeddingProvider::Ollama {
            base_url: default_ollama_url(),
            model: default_ollama_model(),
            dimensions: default_dimensions(),
        }
    }
}

#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct ProjectConfig {
    pub name: String,
    pub description: String,
    pub main_language: String,
    pub team_size: Option<u32>,
    pub custom_notes: Option<String>,
}

#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct ScanConfig {
    /// Extra dirs to exclude beyond .gitignore
    pub exclude_dirs: Vec<String>,
    /// File extensions to include (empty = all)
    pub include_extensions: Vec<String>,
    /// Max file size in KB to index content (not just path)
    pub max_file_size_kb: u64,
    /// Max depth to traverse
    pub max_depth: usize,
}

#[derive(Debug, Serialize, Deserialize, Clone)]
pub struct OutputConfig {
    pub output_file: String,
    /// Include file tree in output
    pub include_tree: bool,
    /// Include key file snippets
    pub include_snippets: bool,
    /// Max lines per snippet
    pub snippet_lines: usize,
    /// Extra free-form instructions for AI agents
    pub agent_instructions: Option<String>,
}

