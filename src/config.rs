impl Config {
    pub fn load_or_default(path: &str) -> Self {
        let config_path = format!("{}/codexa.toml", path);
        if let Ok(content) = std::fs::read_to_string(&config_path) {
            toml::from_str(&content).unwrap_or_default()
        } else {
            Config::default()
        }
    }

    pub fn save(&self, path: &str) -> Result<()> {
        let config_path = format!("{}/codexa.toml", path);
        let content = toml::to_string_pretty(self)?;
        std::fs::write(config_path, content)?;
        Ok(())
    }

    /// Simple interactive wizard (no external TUI deps — keeps MVP lean)
    pub fn interactive_create(path: &str) -> Result<Self> {
        let mut cfg = Config::load_or_default(path);

        cfg.project.name = prompt("Project name", &cfg.project.name)?;
        cfg.project.description = prompt("Short description", &cfg.project.description)?;
        cfg.project.main_language = prompt("Main language (rust/typescript/python/...)", &cfg.project.main_language)?;

        let instructions_default = cfg
            .output
            .agent_instructions
            .clone()
            .unwrap_or_default();
        let instructions = prompt("Agent instructions (rules for the AI)", &instructions_default)?;
        cfg.output.agent_instructions = if instructions.is_empty() {
            None
        } else {
            Some(instructions)
        };

        let notes = prompt("Extra notes for AI (optional)", "")?;
        cfg.project.custom_notes = if notes.is_empty() { None } else { Some(notes) };

        let include_snippets = prompt("Include key file snippets? (y/n)", "y")?;
        cfg.output.include_snippets = include_snippets.trim().to_lowercase() != "n";

        Ok(cfg)
    }
}

fn prompt(label: &str, default: &str) -> Result<String> {
    if default.is_empty() {
        print!("  {} : ", label);
    } else {
        print!("  {} [{}] : ", label, default);
    }
    io::stdout().flush()?;

    let mut input = String::new();
    io::stdin().read_line(&mut input)?;
    let trimmed = input.trim().to_string();

    Ok(if trimmed.is_empty() {
        default.to_string()
    } else {
        trimmed
    })
}

impl Config {
    /// Append vector wizard logic — called by interactive_create after base config.
    pub fn configure_vector_wizard(&mut self) -> Result<()> {
        use colored::Colorize;

        println!();
        println!("  {} Vector database speeds up search on large projects.", "→".cyan());
        println!("    Backends: {} (zero setup) | {} (needs pgvector)", "sqlite".bold(), "postgres".bold());

        let use_vector = prompt("Enable vector database? (y/n)", "n")?;
        if use_vector.trim().to_lowercase() != "y" {
            self.vector = None;
            return Ok(());
        }

        let backend_choice = prompt("Backend: sqlite or postgres", "sqlite")?;
        let backend = if backend_choice.trim() == "postgres" {
            let conn = prompt(
                "PostgreSQL connection string",
                "host=localhost dbname=codexa user=dev password=dev",
            )?;
            VectorBackend::Postgres { connection_string: conn }
        } else {
            let db_path = prompt("SQLite DB path", ".codexa/index.db")?;
            VectorBackend::Sqlite { path: db_path }
        };

        println!();
        println!("  Embedding provider:");
        println!("    2) openai  — cloud, accurate");
        println!("    3) openai_compatible — any compatible API");
        println!("    4) local_onnx — local, private, no external deps (advanced)");
        let emb_choice = prompt("Provider (1-4)", "1")?;

        let embedding = match emb_choice.trim() {
            "2" => {
                let key = prompt("OpenAI API key", "")?;
                EmbeddingProvider::OpenAI {
                    api_key: key,
                    model: "text-embedding-3-small".into(),
                }
            }
            "3" => {
                let base = prompt("API base URL", "http://localhost:8080/v1")?;
                let model = prompt("Model name", "nomic-embed-text")?;
                let dims: usize = prompt("Embedding dimensions", "768")?.parse().unwrap_or(768);
                EmbeddingProvider::OpenAICompatible {
                    api_base: base,
                    api_key: None,
                    model,
                    dimensions: dims,
                }
            }
            "4" => {
                let m_path = prompt("Path to model (.onnx)", ".codexa/models/model.onnx")?;
                let t_path = prompt("Path to tokenizer.json", ".codexa/models/tokenizer.json")?;
                let dims: usize = prompt("Embedding dimensions", "384")?.parse().unwrap_or(384);
                EmbeddingProvider::LocalOnnx {
                    model_path: m_path,
                    tokenizer_path: t_path,
                    dimensions: dims,
                }
            }
            _ => EmbeddingProvider::Ollama {
                base_url: "http://localhost:11434".into(),
                model: "nomic-embed-text".into(),
                dimensions: 768,
            },
        };

        self.vector = Some(VectorConfig { backend, embedding, ..VectorConfig::default() });
        Ok(())
    }
}
